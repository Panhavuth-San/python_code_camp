#Write a python function that receives a list of integers in the arguments and return the mean (Average) of the list elements.
args = [1,2,3,4,5,10,20]
def mean_of_list(args):
    total = sum(args)
    total_element = len(args)
    total // total_element
    return total // total_element
print(mean_of_list(args))

